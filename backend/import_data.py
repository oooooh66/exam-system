# ============================================
# 去重 + JSON 正确编码的插入函数
# ============================================
def insert_question(qtype, content, opts_list, answer, analysis, cat_name, diff, score, org_id, org_nm, created_by_id=1):
    """
    插入一道题目：
    - 判断题干是否已存在，存在则跳过
    - JSON 字段通过 raw SQL UPDATE 写入，确保中文不转义
    """
    # 去重检查
    if BusiQuestion.objects.filter(content=content, is_deleted=False).exists():
        return 0  # skipped

    # 先创建记录（JSON 字段用合法 JSON 值占位，后续 UPDATE 修正）
    obj = BusiQuestion.objects.create(
        question_type=qtype,
        content=content,
        options='[]',              # 合法 JSON（空数组）
        correct_answer='""',       # 合法 JSON（空字符串）
        analysis=analysis,
        category=cats[cat_name],
        difficulty=diff,
        default_score=score,
        org_id=org_id,
        org_nm=org_nm,
        created_by_id=created_by_id,
    )

    # 用 raw SQL 更新 JSON 字段，确保中文不被转义为 \uXXXX
    opts_json = json.dumps(opts_list, ensure_ascii=False)
    ans_json = json.dumps(answer, ensure_ascii=False) if isinstance(answer, (list, str)) else json.dumps(str(answer), ensure_ascii=False)

    with connection.cursor() as cursor:
        cursor.execute(
            "UPDATE busi_questions SET options = %s, correct_answer = %s WHERE id = %s",
            [opts_json, ans_json, obj.id]
        )
    return 1  # inserted

